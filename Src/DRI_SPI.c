/******************************************************************************/
/*  @函数名   : SPI1_SendByte                                                 */
/*  @输入     : byte:输出数据                                                 */
/*  @输出     : 返回数据                                                      */
/*  @修改时间 : 2014-06-03                                                    */
/*  @描述     : SPI单字节发送                                                 */
/******************************************************************************/
HAL_StatusTypeDef SPI1_SendByte(uint8_t byte)
{
    return HAL_SPI_Transmit(&hspi1, &byte ,1,1000);
}

/******************************************************************************/
/*  @函数名   : SPI1_ReadByte                                                 */
/*  @输入     : byte:输出数据                                                 */
/*  @输出     : 返回数据                                                      */
/*  @修改时间 : 2014-06-03                                                    */
/*  @描述     : SPI单字节发送                                                 */
/******************************************************************************/
uint8_t SPI1_ReadByte()
{
    uint8_t uReceive = 0;
    HAL_SPI_Receive(&hspi1, &uReceive ,1,1000);
    return uReceive;
}
